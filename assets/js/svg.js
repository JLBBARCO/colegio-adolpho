function renderSVGs() {
  icons.forEach((icon) => {
    const elements = document.querySelectorAll(`i.${icon.className}`);
    elements.forEach((element) => {
      element.innerHTML = `${icon.svg}`;
    });
  });
}

// Função para atualizar o ícone de contraste
function updateContrastIcon() {
  const iconContrast = document.getElementById("iconContrast");
  if (!iconContrast) return;

  // Encontra o ícone correto na lista
  const iconData = icons.find((icon) => {
    const shouldShow = iconContrast.classList.contains(icon.className);
    return (
      shouldShow && (icon.className === "moon" || icon.className === "sun")
    );
  });

  if (iconData) {
    iconContrast.innerHTML = iconData.svg;
  }
}

// Renderiza SVGs quando o DOM estiver pronto
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", renderSVGs);
} else {
  renderSVGs();
}
